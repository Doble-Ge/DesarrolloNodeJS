export class Usuario {
    id: number
    nombre: string
    apellido: string
    email: string
    pass: string
    cuil: string
    perfila_id: number
    primerLogin: boolean
    

    constructor(){
        this.id = this.id
        this.nombre = this.nombre
        this.apellido = this.apellido
        this.email = this.email
        this.pass = this.pass
        this.cuil = this.cuil
        this.perfila_id = this.perfila_id
        this.primerLogin = true
        
    }
}